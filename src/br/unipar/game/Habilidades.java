package br.unipar.game;

public class Habilidades {

    private String falar;
    private boolean disparar;
    private boolean correr;
    private boolean recarregar;
    private boolean andar;
    private boolean agaixar;
    private String largar_item;
    private String pegar_item;
    private String inspecionar_item;

    public String getFalar() {
        return falar;
    }

    public void setFalar(String falar) {
        this.falar = falar;
    }

    public boolean isDisparar() {
        return disparar;
    }

    public void setDisparar(boolean disparar) {
        this.disparar = disparar;
    }

    public boolean isCorrer() {
        return correr;
    }

    public void setCorrer(boolean correr) {
        this.correr = correr;
    }

    public boolean isRecarregar() {
        return recarregar;
    }

    public void setRecarregar(boolean recarregar) {
        this.recarregar = recarregar;
    }

    public boolean isAndar() {
        return andar;
    }

    public void setAndar(boolean andar) {
        this.andar = andar;
    }

    public boolean isAgaixar() {
        return agaixar;
    }

    public void setAgaixar(boolean agaixar) {
        this.agaixar = agaixar;
    }

    public String getLargar_item() {
        return largar_item;
    }

    public void setLargar_item(String largar_item) {
        this.largar_item = largar_item;
    }

    public String getPegar_item() {
        return pegar_item;
    }

    public void setPegar_item(String pegar_item) {
        this.pegar_item = pegar_item;
    }

    public String getInspecionar_item() {
        return inspecionar_item;
    }

    public void setInspecionar_item(String inspecionar_item) {
        this.inspecionar_item = inspecionar_item;
    }
}


